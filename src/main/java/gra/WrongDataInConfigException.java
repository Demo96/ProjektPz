package gra;

public class WrongDataInConfigException extends Exception {
	public WrongDataInConfigException() { super(); }
	  public WrongDataInConfigException(String message) { super(message); }
	  public WrongDataInConfigException(String message, Throwable cause) { super(message, cause); }
	  public WrongDataInConfigException(Throwable cause) { super(cause); }
}
